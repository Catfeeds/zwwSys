package com.zwwSys.user.vo;

public class GrabVo {
	
	private String id;
	
	private String totals;
	
	private String date;
	
	private String grabNumbers;
	
	private String catchNumbers;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTotals() {
		return totals;
	}

	public void setTotals(String totals) {
		this.totals = totals;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getGrabNumbers() {
		return grabNumbers;
	}

	public void setGrabNumbers(String grabNumbers) {
		this.grabNumbers = grabNumbers;
	}

	public String getCatchNumbers() {
		return catchNumbers;
	}

	public void setCatchNumbers(String catchNumbers) {
		this.catchNumbers = catchNumbers;
	}

}
